写一个和上节练习类似的游戏。同类的任何题材的游戏都可以，花一个星期让它
尽可能有趣一些。作为加分练习，你可以尽量多使用数组、函式、以及模块（记
得练习 13 吗？），而且尽量多弄一些新的 Ruby 程式让你的游戏跑起来。
过有一点需要注意，你应该把游戏的设计先写出来。在你开始写代码之前，你应
该设计出游戏的地图，创建出玩家会碰到的房间、怪物、以及陷阱等环节。
一旦搞定了地图，你就可以写写代码了。如果你发现地图有问题，就调整一下地
图，让写代码和地图互相符合。
最后一个建议：每一个开发人员在开始一个新的大项目时，都会被非理性的恐惧
影响到。为了避免这种恐惧，他们会拖延时间，到最后一事无成。我有时会这样，
每个人都会有这样的经历，避免这种情况的最好的方法是把自己要做的事情列出
来，一次完成一样。
开始做吧。先做一个小一点的版本，扩充它让它变大，把自己要完成的事情一一
列出来，然后逐个完成就可以了。
