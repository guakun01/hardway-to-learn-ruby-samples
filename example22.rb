练习 22: 到现在你学到了哪些东西
这节以及下一节的练习中不会有任何代码，所以也不会有练习答案或者加分练习。
其实这节练习可以说是一个巨型的加分练习。我将让你完成一个表格，让你回顾
你到现在学到的所有东西。
首先，回到你的每一个练习的脚本里，把你碰到的每一个词和每一个符号
（symbol，character 的别名）写下来。确保你的符号列表是完整的。下一步，
在每一个关键词和符号后面写出它的名字，并且说明它的作用。如果你在书里找
不到符号的名字，就上网找一下。如果你不知道某个关键字或者符号的作用，就
回到用到该符号的章节通读一下，并且在脚本中测试一下这个符号的用处。
你也许会碰到一些横竖找不到答案的东西，只要把这些记在列表里，它可以提示
你让你知道还有哪些东西不懂，等下次碰到的时候，你就不会轻易跳过了。等你
记住了这份列表中的所有内容，就试着把这份列表默写一遍。如果发现自己漏掉
或者无法从「记忆中」回想起某些内容，就回去再记一遍。
Warning: 做这节练习没有失败，只有尝试，请牢记这一点。
